    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        Integer codigo = Integer.parseInt(textCodigo.getText());

        Contato c = null;
        try {
            c = this.getPorCodigo(codigo);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Deu erro. "+
                   ex.getLocalizedMessage());
        }


        if (c != null) {
            textNome.setText(c.getNome());
            textCep.setText(c.getCep().toString());
            textComplemento.setText(c.getComplemento());
            textEmail.setText(c.getEmail());
            textEmailAlt.setText(c.getEmailAlternativo());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Código Não encontrado");
        }
    }//GEN-LAST:event_buscarActionPerformed

