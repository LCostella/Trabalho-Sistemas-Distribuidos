    public Contato getPorCodigo(Integer codigo) throws SQLException {
        String sql = "SELECT contato.codigo, contato.nome, contato.email, contato.complemento, contato.endereco, contato.emailalaternativo " +
                    "FROM contato" +
                    "WHERE codigo = " + "'" + codigo + "'" + ";";
        Statement st = Conexao.getStatement();
        ResultSet rs = st.executeQuery(sql);
        Contato c = null;
        if (rs.next()) {
            c = new Contato();
            c.setCodigo(rs.getInt("codigo"));
            c.setNome(rs.getString("nome"));
            c.setEmail(rs.getString("email"));
            //c.setCep(cep);
            c.setComplemento(rs.getString("complemento"));
            c.setEndereco(rs.getString("endereco"));
            c.setEmailAlternativo(rs.getString("emailalaternativo"));
        }
        return c;    
    } 
    
